/* ========= 自然な文末調整（Mirra 互換） ========= */

export function naturalClose(text: string): string {
  if (!text) return text;
  const t = text.trim();
  if (/[。.!?！？」\)]$/.test(t)) return t;
  return `${t}。`;
}
